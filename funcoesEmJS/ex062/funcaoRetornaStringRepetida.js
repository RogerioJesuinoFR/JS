/*escreva uma função que receba um número e retorne uma string repetida aquele número de vezes*/

function repeteString(num){

    //O método repeat da string, que contrói e retorna uma nova string que contém o número especificado de cópias da string original, concatenadas juntas
    
    return 'Olá'.repeat(num);
}
console.log(repeteString(3));